                <p class="text-muted">A principal vantagem dos consórcios é que não embutem juros e os custos tendem a ser mais baixos que os dos financiamentos. De qualquer forma, são necessários alguns cuidados antes de aderir à modalidade:</p>

                <h2>• Contrato</h2>
                <p class="text-muted">Antes de tomar a decisão, é importante ler  atentamente todas as cláusulas do contrato. </p>
