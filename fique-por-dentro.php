                <h2>Como acontece o sorteio?</h2>
                <p class="text-muted">O Sorteio é baseado na extração da loteria federal coletada antes da assembleia de contemplação. Desta, participam somente os consorciados que estiverem com o pagamento das prestações em dia.</p>

                <h2>O que acontece após ser contemplado?</h2>
                <p class="text-muted">De posse da carta de crédito, o consorciado escolhe o bem que irá adquirir e, após apresentar as garantias exigidas pelo contrato, terá a liberação do pagamento. Vale ressaltar que as prestações deverão continuar sendo pagas até o encerramento do grupo.</p>

                <h2>Posso alterar o bem?</h2>
                <p class="text-muted">Pode, caso a cota ainda não tenha sido contemplada.</p>

