                <h1 class="wow fadeIn" data-wow-delay=".3s">Glossário</h1>

                <h2>Adesão</h2>
                <p class="text-muted">Momento da assinatura do contrato com a aquisição da cota pelo consorciado e aceite das condições do plano de consórcio, passando a integrar um determinado grupo.</p>

                <h2>Administradora de Consórcio</h2>
                <p class="text-muted">É a empresa especializada na organização e gestão de grupos de consórcio para a aquisição de bens. Precisa da autorização do Banco Central para funcionar.</p>

                <h2>Assembleias</h2>
                <p class="text-muted">A assembleia é a reunião mensal dos participantes do grupo de consórcio. A primeira assembleia de um grupo de consórcio, chamada de Assembleia de Constituição, define as características do grupo. As assembleias seguintes, chamadas Assembleias Gerais Ordinárias (AGO), ocorrem mensalmente para realizar contemplações, prestação de informações e atendimento aos consorciados. Existe, ainda, a Assembleia Geral Extraordinária (AGE), que pode ser convocada pela administradora ou pelo grupo para deliberar, em caráter extraordinário, sobre assuntos relativos à operação do grupo de consórcio.</p>

                <h2>Carta de crédito</h2>
                <p class="text-muted">Valor colocado à disposição pela Administradora quando da contemplação do bem adquirido pelo consorciado após a análise e aprovação do crédito e do bem.*
                <br/><small>* sujeita a análise de crédito</small></p>

                <h2>Contemplação</h2>
                <p class="text-muted">É a atribuição ao consorciado do direito de utilizar o crédito, representado pela carta de crédito.</p>
                <p class="text-muted">A contemplação ocorre por sorteio ou lance.</p>

                <h2>Contrato de adesão</h2>
                <p class="text-muted">É o instrumento que, firmado pelo consorciado e pela administradora, cria vínculo jurídico entre as partes e pelo qual o consorciado formaliza seu ingresso em grupo de consórcio, estando nele expressas as condições de operação dos grupos de consórcio.</p>

                <h2>Consorciado</h2>
                <p class="text-muted">É a pessoa física ou jurídica que integra o Grupo de Consórcio como titular de cota e assume a obrigação de contribuir para o cumprimento integral de seus objetivos.</p>

                <h2>Consórcio</h2>
                <p class="text-muted">É um sistema de compra que possibilita a aquisição programada de bens móveis, imóveis ou serviços de forma autofinanciada, com capital próprio, sem utilização de empréstimos e com parcelas sem juros.</p>

                <h2>Cota</h2>
                <p class="text-muted">Representa a participação do consorciado no grupo. É identificada por um número, determinado após a adesão do consorciado.</p>

                <h2>Fundo Comum</h2>
                <p class="text-muted">Corresponde aos recursos que serão utilizados para entrega das cartas de crédito aos consorciados contemplados. Será constituído pelo valor correspondente a contribuição dos consorciados e o valor dos rendimentos obtidos com a aplicação financeira dos recursos do próprio fundo.</p>

                <h2>Fundo de Reserva</h2>
                <p class="text-muted">Fundo criado através de pagamentos  do grupo de consorciados, tendo como objetivo custear eventuais defasagens financeiras que apareçam durante a existência do grupo de consórcio.</p>

                <h2>Grupo de consórcio</h2>
                <p class="text-muted">É a reunião de pessoas físicas e/ou jurídicas, reunidas pela administradora, com prazo de duração e número de cotas previamente determinados, com a finalidade de propiciar a seus integrantes a aquisição de bem por meio de autofinanciamento. Ressalta-se que o interesse do grupo prevalece sobre os interesses individuais dos consorciados.</p>

                <h2>Seguro Prestamista</h2>
                <p class="text-muted">Será disponibilizado ao consorciado de forma opcional. O prêmio do seguro será pago pelo consorciado juntamente com a prestação mensal.</p>

                <h2>Taxa de Administração</h2>
                <p class="text-muted">É a remuneração paga pelo consorciado a administradora pelos serviços prestados para a formação, organização e administração do grupo de consórcios.</p>
