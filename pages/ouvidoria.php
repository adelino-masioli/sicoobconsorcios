                <h2>Finalidade</h2>
                <p class="text-muted">O canal de Ouvidoria tem a finalidade de atuar como canal de comunicação entre os usuários dos produtos e serviços, que já tiveram acesso aos canais de atendimentos habituais e não ficaram satisfeitos com a solução.</p>

                <h2>Atribuições</h2>
                <p class="text-muted">• Prestar os esclarecimentos necessários e dar ciência aos reclamantes acerca do andamento de suas demandas e das providências adotadas.</p>
                <p class="text-muted">• Informar aos reclamantes o prazo previsto para resposta final.</p>
                <p class="text-muted">• Encaminhar resposta para a demanda dos reclamantes dentro do prazo informado.</p>

                <h2>Identificação</h2>
                <p class="text-muted">É necessário que o demandante informe todos os dados de identificação obrigatórios, para que seja possível registrar a reclamação.</p>

                <h2>Tratamento e solução</h2>
                <p class="text-muted">Durante a apuração, caso seja identificada necessidade, as equipes envolvidas na solução poderão realizar contatos com o demandante. Por esse motivo, o cliente deve garantir que as informações obrigatórias sejam informadas corretamente.</p>
                <p class="text-muted">A solução da demanda será disponibilizada ao cliente, de acordo com a opção de resposta escolhida. A demanda, quando registrada no canal de Ouvidoria, é identificada por meio de número de protocolo informado ao cliente ao final do registro.</p>

                <h2>Prazo</h2>
                <p class="text-muted">O prazo para atendimento das demandas é de até 10 dias úteis, contados da data de protocolo da ocorrência, podendo ser prorrogado por mais 10 dias úteis.</p>
