                <p class="text-muted">Aquisição de Cotas
                    A aquisição de cotas do Sicoob Consórcios poderá ser feita junto às cooperativas de crédito do Sicoob, mediante assinatura de Contrato de Adesão, no qual são especificadas todas as condições negociais.  Após a assinatura do primeiro contrato, dá-se início ao processo de formação do grupo, que será oficialmente constituído na data da primeira Assembleia Geral Ordinária. Nela, será realizada a contemplação inicial.</p>
